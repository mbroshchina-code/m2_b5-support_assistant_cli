"""Классификация пользовательских запросов и правила эскалации.

Определяет 4 категории (faq, проблема), словари
ключевых слов для эвристической классификации
"""

from __future__ import annotations

from m2_b5.models import Category

PROBLEM_KEYWORDS = {
    "ошибка",
    "не работает",
    "не могу",
    "нет сети",
    "нет интернета",
    "проблема",
    "загрузка",
    "вход",
    "синхронизация",
    "не получается",
    "не включается",
    "выдает",
    "выводится",
    "черный экран",
    "погас",
    "не проходит оплата",
    "не могу",
    "не дает",
    "что делать",
    "как исправить",
    "пропало",
    "нет связи",
}

FAQ_KEYWORDS = {
    "сколько стоит",
    "привет",
    "здравствуйте",
    "добрый день",
    "до свидания",
    "тариф",
    "как подключить",
    "как оплатить",
    "как установить",
    "где",
    "можно ли",
    "подобрать приложение",
    "заказать",
    "как найти",
    "какое выбрать",
    "лимит",
    "как продлить",
    "нужно понять, как",
    "оператор",
    "вызвать специалиста",
    "не нравится",
    "заказать специалиста",
    "купить кассу",
    "где взять",
    "позови человека",
    "нужен человек",
}


def heuristic_classify(user_message: str) -> Category:
    text = user_message.lower()
    if any(keyword in text for keyword in PROBLEM_KEYWORDS):
        return Category.PROBLEM
    if any(keyword in text for keyword in FAQ_KEYWORDS):
        return Category.FAQ
    return Category.PROBLEM